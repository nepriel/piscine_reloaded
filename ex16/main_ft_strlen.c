#include <stdio.h>

int	ft_strlen(char *str)
{
	int i;

	i = 0;
	while (str[i])
		i++;
	return (i);
}

int	main(void)
{
	char *s = "0123456789";

	printf("%d", ft_strlen(s));
	return (0);
}
